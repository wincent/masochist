        <h3>Registering Wincent software</h3>
        
        <p>Once you have paid you will receive your license information via email. Messages are sent by a fully-automated system so you should receive the message within seconds or minutes of completing your payment depending on network conditions. If after waiting several minutes you still have not received your license notice please see <a href="http://www.wincent.com/a/support/faq/#licenseCode">this question</a> in the <a href="http://www.wincent.com/a/support/faq/">Frequently Asked Questions</a>.</p>
        
        <h4>Copying license information from your email</h4>
        
        <p>Your license notice indicates your registered email address (next to the label, <tt>EMAIL ADDRESS:</tt>). To prevent other people from using your license, license codes are cryptographically tied to the email address specified at the time of purchase. This means that when registering you must enter your email address exactly as it appears on the license notice. If you enter a different email address it will not work. <strong>Given the need for accuracy it is recommended that you use copy and paste to enter your email address in the purchased application.</strong></p>
        
        <p>It's very important to keep backups of your license notice (both printed to paper and electronic, preferably burnt to a CD or DVD) so that if you ever lose access to your email address then you will still have a copy of your registration information. For security reasons, license codes are permanently bound to the email address used at the time of purchase. This is to reduce the likelihood of an outsider attempting to "hijack" your license by claiming to be you and then trying to change the address associated with the license. If you want to notify me of a change of address, for my own records, you can use <a href="http://www.wincent.com/a/support/update-address/">this form</a>. I will keep this information on file, but I won't be able to issue you with a new license-code for the reasons already stated.</p>
        
        <p>Your license notice also indicates your unique license code (next to the label, <tt>LICENSE CODE:</tt>). The code is quite lengthy because it is generated using strong cryptography. <strong>Given the length of the code it is recommended that you use copy and paste to enter it in the purchased application.</strong> The following screen snippet shows a sample license code already highlighted in the mail reader, ready to be copied and pasted into the target application.</p>
        
        <div class="screenshots">
          <img src="license-email.png" alt="Emailed license information" width="313" height="160" />
        </div>
        
        <h4>Entering your email address</h4>
        
        <p>The following screen snippet shows what the registration panel looks like after pasting the sample registered email address into the first text field. Note that the cursor has been moved down into the first text field of the license code area, ready for pasting.</p>
        
        <div class="screenshots">
          <img src="entering-license-1.png" alt="Preparing to enter license code" width="531" height="429" />
        </div>
        
        <p>You don't have to worry about any additional leading or trailing whitespace when performing the copy-and-paste as this will automatically be stripped for you. Email addresses are not case-sensitive, so you don't have to worry about that either.</p>
        
        <h4>Entering your license code</h4>
        
        <p>The screen snippet below shows what the registration panel looks like after pasting the sample license code. Note that it is not necessary (or desirable) to type out the entire code manually, nor should you paste the code in separate chunks. You can paste the entire code in one go and the software will automatically distribute it correctly across all of the text fields in the matrix. The text fields are for visual separation only; in reality they behave together like one big text field.</p>
        
        <div class="screenshots">
          <img src="entering-license-2.png" alt="Preparing to enter license code" width="530" height="428" />
        </div>
        
        <p>You don't have to worry about any additional leading or trailing whitespace when performing the copy-and-paste as this will automatically be stripped for you. License codes are not case-sensitive, so you don't have to worry about that either.</p>
        
        <h4>Verifying your license</h4>
        
        <p>Once you have copied and pasted both the registered email address and license code you can click the "Verify" button to complete the registration process. This is a two step process:
          <ol>
            <li><em>The application checks that your license code is valid:</em> if the application reports that your license code is invalid, please double-check that you have correctly pasted both the registration email address and the license code from your license notice. If after double-checking you are still having trouble try asking a friend to help; often a second pair of eyes can spot a mistake that may not be obvious to you. If you're unable to enter your license code even after asking a friend you can <a href="https://secure.wincent.com/a/support/tickets/">open a support ticket</a> (although please be aware that the advice you receive is unlikely to go very far beyond the information already on this page!).</li>
            <li><em>The application performs product activation</em>: this is a once-only process that is fully described on the <a href="http://www.wincent.com/a/support/activation/">product activation page</a> and the <a href="http://www.wincent.com/a/support/activation/privacy/">product activation privacy FAQ</a>. If you are having troubles with product activation after consulting those pages please feel free to <a href="https://secure.wincent.com/a/support/tickets/">open a support ticket</a>.</li>
          </ol>
          
