<h2>December 23, 2007</h2>

<h3>Version numbers</h3>

<p>I've always been a little confused by <a href='http://www.wincent.com/knowledge-base/Apple'>Apple</a>'s &quot;<a href='http://developer.apple.com/documentation/MacOSX/Conceptual/BPRuntimeConfig/Articles/PListKeys.html'>Property List Key Reference</a>&quot;. It seemed that the documented purpose of some of the property list keys deviated from the actual behaviour of the OS, especial in relation with the display of version numbers in the Finder.</p>
<p>The documentation seems to have undergone a number of <a href='http://developer.apple.com/documentation/MacOSX/Conceptual/BPRuntimeConfig/RevisionHistory.html'>revisions</a> lately (&quot;Undocumented the CFBundleGetInfoString key&quot;, &quot;Added details on the new purpose of the CFBundleGetInfoString key&quot;, &quot;Reintroduced the CFBundleGetInfoString key&quot;) and this is the current state of affairs under <a href='http://www.wincent.com/knowledge-base/Leopard'>Leopard</a>:</p>
<a name='Behaviour'></a><h2>Behaviour</h2>

<p>This is really the only thing you need to care about.</p>
<p>The value of <tt>CFBundleGetInfoString</tt> is shown in Finder &quot;Get Info&quot; window. An appropriate value for this   key might be something like &quot;MyApplication 1.2 (332), Copyright 2007 Me&quot;; in other words, the application name, the release version, the build number, and the copyright info. This key can be localized.</p>
<p>A string consisting of &quot;<tt>CFBundleShortVersionString (CFBundleVersion)</tt>&quot; is shown in your application  &quot;About&quot; box. A typical value for <tt>CFBundleShortVersionString</tt> would be something like &quot;Version 1.2&quot;. This key can and often is localized, principally because of the presence of the word &quot;Version&quot;. The <tt>CFBundleVersion</tt> is usually a build number, like &quot;332&quot; in the example above. There's no reason to localize this and in fact Apple's docs say that the key isn't localizable.</p>
<p>As far as I can tell, the <tt>NSHumanReadableCopyright</tt> key can be effectively ignored.</p>


<div id="a003357more"><div id="more">
<a name='The%20docs'></a><h2>The docs</h2>

<p>So what do the docs say? It seems that of late the behaviour, at least in Leopard, and the documentation match up a lot better than they used to.</p>
<a name='%3Ctt%3ECFBundleGetInfoString%3C%2Ftt%3E%3A%20a%20%26quot%3BBrief%20description%20of%20the%20bundle%26quot%3B'></a><h3><tt>CFBundleGetInfoString</tt>: a &quot;Brief description of the bundle&quot;</h3>

<blockquote>This key identifies a brief description of the bundle. For an application bundle, this key provides a short description of the application or the current release that the build or release version number cannot convey; for example, the date of the release. This key can be localized.</blockquote>
<a name='%3Ctt%3ECFBundleVersion%3C%2Ftt%3E%3A%20%26quot%3BThe%20build-version-number%20string%20for%20the%20bundle%26quot%3B'></a><h3><tt>CFBundleVersion</tt>: &quot;The build-version-number string for the bundle&quot;</h3>

<blockquote>This key specifies the build version number of the bundle, which identifies an iteration (released or unreleased) of the bundle. This is a monotonically increased string, comprised of one or more period-separated integers. This key is not localizable.</blockquote>
<a name='%3Ctt%3ECFBundleShortVersionString%3C%2Ftt%3E%3A%20%26quot%3BThe%20release-version-number%20string%20for%20the%20bundle%26quot%3B'></a><h3><tt>CFBundleShortVersionString</tt>: &quot;The release-version-number string for the bundle&quot;</h3>

<blockquote>This key specifies the release version number of the bundle, which identifies a released iteration of the application. The release version number is a string comprised of three period-separated integers. The first integer represents major revisions to the application, such as revisions that implement new features or major changes. The second integer denotes revisions that implement less prominent features. The third integer represents maintenance releases. The value for this key differs from the value for &quot;CFBundleVersion&quot;, which identifies an iteration (released or unreleased) of the application.</blockquote>
<a name='%3Ctt%3ENSHumanReadableCopyright%3C%2Ftt%3E%3A%20%26quot%3BSpecifies%20the%20copyright%20notice%20for%20the%20bundle%26quot%3B'></a><h3><tt>NSHumanReadableCopyright</tt>: &quot;Specifies the copyright notice for the bundle&quot;</h3>

<p>Finally it's worth mentioning this one:</p>
<blockquote>This key contains a string with the copyright notice for the bundle; for example, <tt>© 2006, My Company</tt>. You can load this string and display it in an About dialog box. This key can be localized by including it in your InfoPlist.strings files. This key replaces the obsolete CFBundleGetInfoString key.</blockquote>
<p>This is the one case here the documentation still seems to be a little off. Sure, you can explicitly load this string and use it in your About box, but the default implementation provided by Cocoa won't touch it, and the Finder won't either.</p>
<a name='Conclusion'></a><h2>Conclusion</h2>

<p>So there you have it. The docs make it very clear that your build numbers should be &quot;monotonically increasing strings&quot; comprising &quot;one or more period-separated integers&quot;. Great if you use <a href='http://www.wincent.com/knowledge-base/Subversion'>Subversion</a>-based build numbers; you'll need to ignore those rules if your build numbers are actually SHA-1 hashes (based on <a href='http://www.wincent.com/knowledge-base/Git'>Git</a> commits, for example). Likewise your release versions are supposed to be of the form &quot;x.y.z&quot;; bad luck if you want to do &quot;1.0-rc1&quot; or &quot;2.1b3&quot; and the like, but it seems unlikely that any harm will come from not following these rules (or are they guidelines?) to their fullest.</p>

