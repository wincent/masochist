<h2>April  1, 2006</h2>


<h3 id="a000272">Radar #4499076: <tt>atos</tt> vs <tt>@executable_path</tt></h3>

<p>Unfortunately it's something I need to do in order to decipher crash reports.When running "atos" on my binaries with the "-o" switch the utility fails to work and I get error messages like the following; in this example we are talking about an executable that links against an embedded copy of the Growl framework:2006-04-01 17:16:12.767 atos[13476] NSMachOHeader -initWithPath::: - no file at "@executable_path/../Frameworks/Growl.framework/Versions/A/Growl"In order to make this error go away I must use "install_name_tool" to get rid of the "@executable_path" reference and change it to an absolute path....  The workaround works but its not very convenient and it requires me to have a copy of any linked frameworks installed at an absolute location.The "atos" man page states:"When working with a Mach-O executable [passed via the '-o' switch], atos considers addresses and symbols defined in that executable, at their default locations, as well as those from both statically and dynamically linked libraries loaded at startup by that executable, at their default locations.</p>
