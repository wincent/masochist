          <h3>General preferences</h3>
          
          <p>The general settings tab is the first that you will see when you choose the Synergy Preference Pane. It contains the following controls:</p>
          
          <ul>
            <li><strong>Start/Stop</strong>: to start and stop Synergy, press the start button; once Synergy is running the button will change to "Stop". Once Synergy is running you will be able to access all of its advanced features, such as Menu bar control buttons, and its global menu (if enabled).</li>
            <li><strong>Launch Synergy at login</strong>: checking this box will cause Synergy to be added to the "Login Items" and will launch every time you log in. When you uncheck this box Synergy is removed from the Login Items.</li>
            <li><strong>Extra visual feedback for play, pause, next and previous track actions</strong>: checking this box results in a semi-transparent feedback window being briefly shown whenever a hot key is used to trigger an action.</li>
            <li>
              <strong>Display Floater when changing tracks</strong>: this feature is displays a semi-transparent window whenever iTunes starts playing a different track. The floater shows available information on track name, album and artist. You can read more about it on the Floater page. If you decide not to enable this box, you can still bring the floater on screen at will by pressing the Show/Hide Floater hot key. The following controls allow you to tailor the appearance of the floater to your liking:
              <ul>
                <li><strong>Include Album information</strong>: when checked, album information is included in the floater whenever available. Availability depends on which audio source iTunes is currently using. For example: when playing mp3s, the information is dependent on the ID3 tags embedded in the files; when playing audio CDs it depends on the information in the CD database; and when streaming Internet Radio, a different set of information is available.</li>
                <li><strong>Include Artist information</strong>: checking this box causes artist information to appear in the floater.</li>
                <li><strong>Include Track duration</strong>: track duration information will be included in the floater when this box is checked.</li>
                <li><strong>Include Year</strong>: if available, the "year" information associated with the currently playing track will appear in the floater.</li>
                <li><strong>Include Rating</strong>: a song rating (from zero to five stars) will appear in the floater.</li>
                <li><strong>Show floater for</strong>: 1 to 20 secs, or always. Here you can adjust the amount of time the floater will remain on screen before fading out of view. If you move the slider all the way towards the infinity symbol at the right, then the floater will remain on screen permanently.</li>
                <li><strong>Transparency</strong>: this slider controls the transparency of the floater's background. As you move to the left, the background becomes more and more see-through (almost invisible); and as you move to the right it becomes more solid (almost black).</li>
                <li><strong>Size</strong>: this slider controls the overall size of the floater, and influences both the size of the fonts used to display information, and the size of the window itself. If you want to put the floater into "mini-mode", simply slide the the control all the way over to the left. The floater then operates in a one-line mode and takes up minimal screen space.</li>
                <li><strong>Graphic</strong>: this pop-up button allows you to choose the type of graphic that will appear in the bottom-left corner of the floater. Available options are "Album cover" (covers downloaded automatically over the Internet), "Synergy icon" and "No graphic".</li>
                <li><strong>Set floater position</strong>: when you press this button you can drag the floater to you desired location anywhere on the main display. A pin icon will show you where the floater anchors itself. When resizing to accommodate track information, the floater will grow away from the pin.</li>
              </ul>
            </li>
          </ul>
          
          <p>Once you are happy with your settings you can use the "Apply" button at the bottom of the Preference Panel to commit your changes permanently. The "Revert" button reverts all settings to the last-saved state, in other words the state they were in the last time you clicked "Apply". The "Defaults" button sets all settings to the standard defaults.</p>
          
          <p>These buttons affect all of the tabs -- "General", "Hot Keys", "Menu Bar" and "About" -- so it is not necessary to click "Apply" in all four tabs; clicking "Apply" once will commit any changes you've made anywhere in the Synergy Preference Pane.</p>
          
