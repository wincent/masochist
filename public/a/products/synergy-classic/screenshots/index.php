        <h3>Synergy screenshots tour</h3>

        <p>
          Welcome to the Synergy tour! The purpose of this tour is to show you
          the major features of Synergy and convince you that this
          <a href="https://wincent.com/a/products/synergy-classic/purchase/"
            >5&euro; (euro) utility</a
          >
          is an essential part of your computing experience!
        </p>

        <img
          style="float:right;"
          src="https://wincent.com/a/products/synergy-classic/screenshots/menubar-controls.jpg"
          alt="Controls"
          width="112"
          height="66"
        />
        <p>
          So what is Synergy? In its most basic form, it is a menu bar control
          interface for iTunes. How many times have you had iTunes playing
          through your music library in "Shuffle" mode, and a song has come on
          that isn't one of your favourites?
        </p>

        <img
          style="float:right;"
          src="https://wincent.com/a/products/synergy-classic/screenshots/alternate-menubar-controls.jpg"
          alt="Controls"
          width="95"
          height="66"
        />
        <p>
          Synergy was the first product on the market that put
          <a href="../buttons/">control buttons</a> for iTunes right in your
          menu bar, where they are only a short click away. It's a simple idea,
          but one that can revolutionise the way you interact with iTunes. With
          these buttons you can skip through one, two, five, ten, an unlimited
          number of songs with simple clicks. Compare this to the time you would
          waste if you had to navigate into iTunes Dock menu, or bring iTunes to
          the front and click on its controls directly. You may only save
          yourself a few seconds, but if you listen to music all day and you
          find yourself skipping tracks hundreds of times a day, those few
          seconds saved turn into minutes.
        </p>

        <h4>
          Synergy allows you to control iTunes without leaving the application
          you are working in, even for an instant
        </h4>

        <p>
          But there is more to Synergy than just rapid mouse-based control over
          iTunes. Your hand might be resting on your mouse all day if you are
          browsing the web, but what if you are typing a letter in a word
          processor or writing a computer program?
        </p>

        <div class="screenshots">
          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/hot-keys-sheet.jpg"
            alt="Hot key customization sheet"
            width="412"
            height="190"
          />
          <p>
            The answer lies in hot keys. Hot keys are customisable key
            combinations that you can press in order to trigger an action in
            iTunes. For example, "Command+Shift+F1", or even just "F10". Synergy
            allows you to rapidly skip tracks, start and stop playback, adjust
            the volume of iTunes, and more without ever having to take your
            hands of the keyboard. Synergy was the first dedicated control
            interface for iTunes to enable you to use hot keys to control
            iTunes.
          </p>

          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/feedback-bezel.jpg"
            alt="Feedback bezel"
            width="250"
            height="249"
          />
          <p>
            But your're not just "flying blind" when you press these hot key
            combinations. Synergy can also provide you with stylish and
            unobtrusive feedback as you trigger actions with hot keys. You can
            have semi-transparent feedback windows like the one shown here
            pop-up whenever you press a hot key combination. This one appears
            when you initiate playback, but other windows give you instant
            feedback when you skip tracks, pause, adjust the iTunes playback
            volume, or adjust the rating of the current song (see below for some
            sample windows).
          </p>

          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/volume-bezel.jpg"
            alt="Volume bezel"
            width="250"
            height="249"
          />
          <p>
            All of Synergy's feedback windows are carefully styled after Apple's
            own semi-transparent feedback windows, so they integrate beautifully
            with your environment. And they won't interfere with your workflow
            in any way, because they rapidly fade out, just like Apple's
            windows; and also like Apple's windows, they are not only visually
            transparent, but also transparent to mouse clicks. You can click
            right through them and interact with whatever happens to be
            underneath.
          </p>

          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/rating-bezel.jpg"
            alt="Rating bezel"
            width="250"
            height="249"
          />
          <p>
            Pictured here is the feedback window that appears when adjusting the
            Rating of the currently playing track. Setting track ratings using
            hot keys is much easier than going into the iTunes dock menu. This
            is one of the many ways in which Synergy will change the way you
            interact with iTunes: the sheer ease with which you can assign
            ratings will mean that before you know it you'll be rating all the
            songs in your library!
          </p>

          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/mini-floater.jpg"
            alt='The Floater in "mini" mode'
            width="363"
            height="33"
          />
          <p>
            But truly interacting with iTunes is a two way process. Synergy not
            only provides you with tools for rapidly commanding iTunes, but with
            an unobtrusive way of knowing what iTunes is doing at every step of
            the way.
          </p>

          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/floater.jpg"
            alt="The Floater"
            width="298"
            height="165"
          />
          <p>
            Pictured above is the Synergy floater. The floater provides an
            attractive and customisable method for showing iTunes status
            information in a rapid yet unobtrusive fashion. Like all the other
            windows in Synergy, the floater is not only transparent in the
            visual sense, but it is transparent to mouse clicks also. Unlike
            other applications on the market which use transparent windows,
            Synergy is transparent to all mouse clicks, no matter what
            application is underneath the transparent window. You can click
            through, initiate drags, and even open contextual and pop-up menus
            through the floater as if it were not there.
          </p>

          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/floater-with-album.jpg"
            alt="The Floater showing an album cover"
            width="465"
            height="153"
          />
          <p>
            Above the floater is shown with the album cover of the currently
            playing song. Synergy can download such covers automatically from
            the Internet in the background, and it stores a copy on your local
            machine so that when you play the album the next time there'll be no
            need to connect to the network to retrieve the image. You can also
            choose "Buy from Amazon.com" in the Synergy global menu and you'll
            instantly be taken to the appropriate page at amazon.com to buy the
            album. If you use iTunes 4, Synergy will try to obtain cover art
            from that before connecting to the Internet.
          </p>
        </div>

        <p>
          As shown below, the is a large range of options for controlling the
          appearance of the floater and the information it contains. You can set
          it to pop up automatically whenever a new track begins playing, in
          which case the floater will smoothly fade in and dynamically resize to
          show you the information you elect to see about the current track. In
          addition to the track title, you have the option of showing the Album,
          the Artist, the Track duration, the Year of recording/composition, and
          the song rating. The ease with which you can both see song ratings in
          the floater and set them using hot keys makes using the rating system
          both effortless and attractive.
        </p>

        <div class="screenshots">
          <img
            src="https://wincent.com/a/products/synergy-classic/screenshots/general.jpg"
            alt="General preferences"
            width="623"
            height="599"
          />
          <p>
            You can also control how long the floater appears on screen, either
            having it fade out after an interval you specify, or remaining on
            screen permanently; and of course you can set its transparency, size
            and position.
          </p>
        </div>

        <h4>
          The combination of the floater and Synergy's controls makes iTunes
