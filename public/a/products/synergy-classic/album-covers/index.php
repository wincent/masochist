          <h3>About the album cover downloads feature</h3>
          
          <p>If you have a connection to the Internet, Synergy can automatically download album covers and display them in the floater (but see the <a href="../news/">news page</a> for any updates about the current status of this feature). These downloads occur in the background so they do not interfere with your workflow,  and the album cover images are stored in your Library folder so that you don't have to download them repeatedly every time you listen to an album. If you have iTunes version 4 or later, Synergy will first query iTunes for album cover art before attempting to connect to the Internet.</p>
          
          <h4>How do I turn on the album cover feature?</h4>
          
          <p>Open the Synergy preferences (either by choosing "Synergy preferences" from the Synergy global menu, or by opening the System Preferences application and switching to the Synergy preference pane). In the "General" tab you will see a pop-up button that allows you to choose the floater graphic. Select "Album cover" and click the Apply button.</p>
          
          <h4>How does Synergy operate when this feature is active?</h4>
          
          <p>Synergy continues to operate normally while attempting to download cover images behind the scenes in background threads. To prevent network congestion, Synergy will never try to download more than one cover per second, but instead will add cover requests to a queue and handle them later on. If a download fails for some reason, Synergy will automatically retry it at a later time.</p>
          
          <p>Effectively, what this means is that you will see the Synergy icon in the floater until an album cover becomes available, at which point the icon will be replaced with the cover image. If you are listening to a song and no album cover appears it is most likely because the download is still in progress in the background, or there is no cover image available on the Internet for that album.</p>
          
          <p>Once a cover is successfully downloaded, a copy is kept in your library folder so that the floater can display the cover without having to access the network.</p>
          
          <h4>Where do the album covers come from?</h4>
          
          <p>If the tracks in your iTunes music library already contain embedded art then Synergy will use that artwork. Note that there are some limitations to that feature; namely it only works for locally-stored music files (iTunes doesn't export album covers for music that is playing across the network via shared playlists, for example).</p>
          
          <p>If iTunes does not provide embedded artwork Synergy connects to amazon.com and searches for an album cover based on the ID3 tag information associated with your music (except where it can simply obtain the artwork from iTunes 4 or later, as described above). The more accurate and complete your ID3 tags, the more likely you are to find a correct match for your album (but note that, by default, there is also filtering -- stripping out words like "CD1" or "Soundtrack" -- performed on ID3 tags to increase the likelihood of a correct search). If a match is found, you will be able to select "Buy from Amazon.com" from the Synergy global menu to be taken directly to the amazon.com page corresponding to that album.</p>
          
          <p>It is very important to try and support Amazon by purchasing items using the "Buy from Amazon.com" menu item whenever possible. During part of 2003 and 2004, Amazon barred Synergy access to their servers because they were seeing too many hits from Synergy users and not enough purchases. I made changes to the Synergy code base to reduce the number of queries made, but you as a user also need to play your part if you wish to see access to Amazon's resources from within Synergy continue. Amazon may choose to cut off access again at any moment. You can also purchase items using <a href="http://www.amazon.com/exec/obidos/redirect-home/synerformacos-20">this link</a>, and Synergy will receive credit for them.</p>
          
          <h4>How does Synergy know which cover to download?</h4>
          
          <p>Synergy first tries to locate a cover using the artist and album name (if that information is available). Failing this, it then tries various combinations of artist, album and song name in an effort to find a match. Not all albums in the amazon.com catalogue have album cover images associated with them, but many of them do.</p>
          
          <h4>Where are the local copies of the album covers stored?</h4>
          
          <p>Local copies are stored at the following location (where "~" stands for your home folder):</p>
          
          <pre>~/Library/Application Support/Synergy/Album Covers/</pre>
          
          <h4>How do I stop Synergy from initiating a dial-up connection when I am not already on the Internet?</h4>
          
          <p>In the "Advanced" tab of the Synergy preferences you will see an option labelled, "Initiate connections to the Internet automatically when required". Turning this option off will prevent Synergy from attempting to download a cover if you are not already connected to the Internet.</p>
          
          <h4>How do I turn off the automatic downloading of covers?</h4>
          
          <p>In the "General" tab of the Synergy preferences, change the floater graphic pop-up button to "Synergy icon" or "No graphic".</p>
          
          <h4>What if Synergy downloads the wrong cover?</h4>
          
          <p>Given that some words in album ID3 tags are relatively common and appear in many, many albums (words like "Greatest hits" and "love", for example), Synergy may not always download the correct cover. Given that Synergy is only a computer program, it cannot search with human intelligence and sort between the false positives and the real album cover; it is limited to a "best guess" based on the fact that the top search result returned by Amazon is most likely the correct one.</p>
          
          <p>In those cases where Synergy downloads the wrong cover the best solution is to do the following:</p>
          
          <ol>
            <li>Locate the correct album cover manually (human intelligence)</li>
            <li>Drag that cover into iTunes</li>
            <li>Delete the incorrectly downloaded cover</li>
          </ol>
          
          <p>If you perform these steps then the next time you play the song the Synergy will display the correct cover (obtained by asking iTunes). You can perform step 3 (deleting the incorrect cover) easily by choosing "Show Album Covers folder..." from the Synergy Global Menu while the track in question is playing. The corresponding cover art file will be displayed (already selected for easy deletion) in the Finder, so that you can quickly hit Command-Delete or drag it to the Trash.</p>
          
          <h4>What does the question mark icon in the floater mean?</h4>
          
          <p>Synergy displays a semi-transparent question mark in the floater where the album art would normally appear to indicate that it hasn't (yet) been able to locate cover art for the current track. This display is usually only temporary, because Synergy is working in the background to search Amazon for the album cover and download it automatically to your system.</p>
          
          <h4>Can Synergy transfer downloaded covers to iTunes?</h4>
          
          <p>Yes, as of version 2.9 Synergy can send downloaded covers to iTunes for embedding directly in the track files. To transfer the current cover art to the current track, choose "Transfer cover art to iTunes" from the Synergy Global Menu. At present this is a conservative implementation (manual rather than automatic, per-track rather than working on multiple tracks at a time). If you would like to transfer the art to multiple tracks in one operation you could try the "Get cover art from Synergy" Script Menu Item in the "Sample Script Menu Items" folder on the Synergy disk image.</p>
          
          <h4>What plans are there to improve this feature in the future?</h4>
          
          <p>There are plans to expand this basic implementation in a number of ways to make it easy for you to assign custom images (for example, cover images that you have scanned yourself) or to cycle through the range of possible matches that Amazon provides. Specifically, plans exist to:</p>
          
          <ul>
            <li>assign custom images via drag-and-drop, or with a standard file dialog</li>
            <li>extract custom images embedded in mp3s via ID3 tags</li>
            <li>look for custom images stored in the same folder as the playing mp3</li>
            <li>cycle through Amazon search results with a hot key, or from the Global menu</li>
          </ul>
          
