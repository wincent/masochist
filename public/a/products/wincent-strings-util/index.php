<h4><a id="v2.0" name="v2.0"></a>2.0 (28 August 2009)</h4>

<ul>
<li>Update man page.</li>
<li>Now requires Mac OS X 10.6 Snow Leopard.</li>
<li>Trim disk footprint by stripping legacy PowerPC code.</li>
<li>Fix Garbage Collection.</li>
</ul>

<p>A code-level changelog for this release is available <a href="history/wincent-strings-util-2.0-release-notes.txt">here</a>.</p>

          <h4><a id="v1.2" name="v1.2"></a>1.2 (6 January 2008)</h4>
          
          <ul>
            <li>New <tt>--extract</tt> and <tt>--combine</tt> switches for even easier incremental localization.</li>
            <li>Support recursive substitution when performing preprocessing on <tt>InfoPlist.strings</tt> files.</li>
            <li>Use double-hyphens for all switches, instead of the less-orthodox single hyphens previously used.</li>
            <li>Add checks for duplicate keys within the same file (this is never correct so the user should be notified about it).</li>
            <li>Stricter parsing of input files when using the <tt>--info</tt> switch; this allows us to detect malformed input files as well guess the input encoding for files which do not have an explicit BOM (Byte Order Mark).</li>
            <li>Allow explicit override of output encoding using the new <tt>--encode</tt> switch.</li>
            <li>Always include an explicit BOM in the output file.</li>
            <li>Add installer package for use with Apple's installer.</li>
            <li>Convert to Objective-C 2.0 and Garbage Collection; as such this release requires Mac OS X Leopard.</li>
            <li>Migrate source code from Subversion to Git and make corresponding adjustments such as removing the embedded $Id$ number from the built executable.</li>
            <li>Update build process to use <a href="https://wincent.com/a/products/buildtools/">Wincent Build Tools</a>.</li>
            <li>Add automated test suite using <a href="http://rspec.info/">RSpec</a>.</li>
          </ul>
          
          <p>A code-level changelog for this release is available <a href="history/1.2-release-notes.txt">here</a>.</p>
          
          <h4><a id="v1.1" name="v1.1"></a>1.1 (17 August 2006)</h4>
          
          <ul>
            <li>User-visible changes:
              <ul>
                <li>Released under the GPL (with Omni's permission)</li>
                <li>Preprocessing support for InpoPlist.strings files</li>
                <li>Errors and warnings now formatted so as to be picked up automatically by Xcode Build Results window</li>
                <li>Warn if cannot confirm UTF-16 encoding of input/output files</li>
              </ul>
            </li>
            <li>Developer-visible changes:
              <ul>
                <li><tt>describeScanLocation</tt> method (verb) renamed to <tt>scanLocationDescription</tt> (noun)</li>
                <li>Fix regression in <tt>peekCharacter</tt> and <tt>scanCharacter</tt> methods (always returned <tt>NO</tt> for last character)</li>
              </ul>
            </li>
          </ul>
          
          <h4><a id="v1.0b" name="v1.0b"></a>1.0b (19 February 2006)</h4>
          
          <ul>
            <li>Initial public release.</li>
            <li>User-visible modifications to Omni stringsUtil:
              <ul>
                <li>work with InfoPlist.strings files (unquoted key strings)</li>
                <li>force output to UTF-16 in all cases (required encoding for strings files)</li>
                <li>executable name is <tt>wincent-strings-util</tt> (change required by license)</li>
                <li>man page (required by license)</li>
                <li>usage now includes copyright info (required by license)</li>
                <li>upgrade project file to "xcodeproj" format, upgrade and rename target</li>
                <li>Universal Binary</li>
                <li>running what(1) on executable produces meaningful output</li>
                <li>non-zero exit code if writing to output file fails</li>
                <li>bail more elegantly (catch exceptions) for malformed input files</li>
                <li>emit warnings to standard error for removed keys</li>
              </ul>
            </li>
            <li>Developer-visible changes to Omni stringsUtil:
              <ul>
                <li>formatting adjustments (trim to fit 80 columns, use GNU99 C syntax)</li>
                <li>extensive refactoring for readability</li>
                <li>changes to scanner methods to more closely match Cocoa conventions</li>
                <li>added assertions</li>
              </ul>
            </li>
          </ul>
          
