          <h3>Version history</h3>
          
          <h4><a id="1.3.1" name="1.3.1"></a>Version 1.3.1 (30 June 2007)</h4>
          
          <ul>
            <li>[Enhancement] Allow one-step installs (see the "What's New" documentation for more information on how this feature can be exploited to provide a better user experience).</li>
            <li>[Enhancement] To increase awareness that Install is also actually an uninstaller, show the "Uninstall" button as ghosted rather than hiding it completely when Install is first launched (<a href="https://wincent.com/a/support/bugs/show_bug.cgi?id=579">feature request #579</a>, "Display Uninstall button even if no previous install detected").</li>
            <li>[Enhancement] When resizing buttons to smaller sizes instruct the superview to redraw in that area also. The previous behaviour was not known to be defective, but this change should proof the code against possible changes in future versions of Mac OS X.</li>
            <li>[Bugfix] Workaround Cocoa bug in which the <tt>sizeToFit</tt> produced buttons that were marginally too narrow.</li>
            <li>[Note] Now ships with unstripped nib files; at the cost of a small increase in download size developers can now edit the nib files in Interface Builder.</li>
          </ul>
          
          <h4><a id="1.3" name="1.3"></a>Version 1.3 (22 November 2005)</h4>
          
          <ul>
            <li>If an error occurs while trying to move files to the Trash without administrator privileges Install now shows a dialog allowing the user to enter an administrator password and try again, reveal the files in the Finder for manual deletion, or cancel the operation.</li>
            <li>Added missing tilde (~) in the default configuration file which prevented developers from accessing the Install manual when pressing the "Documentation" button after installing for the current user only.</li>
            <li>Fixed minor memory leak (less than one kilobyte per run).</li>
            <li>Tested and qualified for use with Mac OS X 10.4, 10.4.1, 10.4.2, 10.4.3 Tiger; Install continues to be compatible with Jaguar and Panther also.</li>
            <li>Built using Xcode 2.2 as a Universal Binary, which means that Install will run natively on both PowerPC and Intel processors. The PowerPC build now explicitly uses the 10.2 SDK which among other things forces GCC 3.3 to be used as the compiler and ensures that 10.2-compatible code is produced.</li>
            
          </ul>
          
          <h4><a id="1.2.3" name="1.2.3"></a>Version 1.2.3 (9 December 2004)</h4>
          
          <ul>
            <li>Added additional checks in the deletion with root privileges routine. There was a very rare crash that could occur here on systems where the "/bin/rm" tool was not present. Fortunately the exact circumstances which provoke the crash are extremely uncommon; there has only been one crash report in 18 months. The additional checking ensures that Install will fail gracefully in this case rather than crash.</li>
            <li>Built using the latest version of Apple's Xcode Tools;</li>
            <li>Introduced new <a href="../licensing/">discount licensing</a> for use with freeware and open source products.</li>
          </ul>
          
          <h4><a id="1.2.2" name="1.2.2"></a>Version 1.2.2 (28 June 2004)</h4>
          
          <ul>
            <li>Rather than providing localized variants of the configuration file, developers now have the option of putting user-visible strings from the file into a separate &quot;InstallConfig.strings&quot; file, simplifying the task of localization for translators;</li>
            <li>More conservative removal of old files when upgrading Install itself.</li>
          </ul>
          
          <h4><a id="1.2.1" name="1.2.1"></a>Version 1.2.1 (23 June 2004)</h4>
          
          <ul>
            <li>A repackaged release issued because some of the improvements in recent versions of Install were not propagated through to the versions distributed to developers;</li>
            <li>On running a &quot;naked&quot; (unconfigured) installer, now displays a dialog before exiting;</li>
            <li>Recompiled with the latest version of Apple's Xcode Tools.</li>
          </ul>
          
          <h4><a id="1.2" name="1.2"></a>Version 1.2 (1 April 2004)</h4>
          
          <ul>
            <li>Can now specify to default to the Global installation domain (useful when omitting the &quot;Domain&quot; step and creating all-user-only installers);</li>
            <li>Fixed bug preventing user from cancelling an uninstall/install after failing to enter a correct administrator username and password.</li>
          </ul>
          
          <h4><a id="1.1.2" name="1.1.2"></a>Version 1.1.2 (29 March 2004)</h4>
          
          <ul>
            <li>Developers may now use the extended error-handling format in &quot;CopyFiles&quot;, &quot;Finish&quot; and &quot;Uninstall&quot; steps, which was previously only available in &quot;Shell&quot; steps;</li>
            <li>Developers wanting to make shorter installers can now omit the &quot;ReadMe&quot; step and use a &quot;License&quot;, &quot;Shell&quot;, &quot;Domain&quot; or &quot;CopyFiles&quot; step as the first step in an installation;</li>
            <li>Further, minor updates to the German localization;</li>
            <li>Corrected error in manual (changed references to the &quot;SCRIPTLOCATION&quot; environment variable to &quot;SCRIPTDIRECTORY&quot;).</li>
          </ul>
          
          <h4><a id="1.1.1" name="1.1.1"></a>Version 1.1.1 (6 March 2004)</h4>
          
          <ul>
            <li>When removing login items developers may now use item names without paths, or full path names;</li>
            <li>When adding or removing login items developers may specify whether any given item should be added/removed if only installing for all users, or if only installing for the current user;</li>
            <li>More control over the response to &quot;Shell&quot; step exit codes; developers can now classify exit codes as &quot;Success&quot;, &quot;Non-fatal&quot;, &quot;Can retry&quot; and &quot;Fatal&quot;;</li>
            <li>If developers specify long button labels in their &quot;Finish&quot; step, the buttons automatically resize to accommodate the text;</li>
            <li>Fixed bug where users would still be asked for an administrator user name and password even though they were only trying to uninstall items which did not require elevated privileges;</li>
            <li>Developers may now optionally specify an RTF or RTFD document to customize the appearance of the &quot;Domain&quot; step;</li>
            <li>Updated Spanish, German and Japanese localizations.</li>
          </ul>
          
          <h4><a id="1.1" name="1.1"></a>Version 1.1 (2 March 2004)</h4>
          
          <ul>
            <li>More sophisticated target version-checking allows Install to use different installation instructions for different versions of Mac OS X, or display a dialog if used to attempt to install software on an incompatible version of the operating system;</li>
            <li>New &quot;Shell script&quot; step allows Install to run shell scripts as part of the installation process (useful for pre-flight/post-flight checks or more complex, custom installation tasks);</li>
            <li>In addition, the &quot;Copy files&quot;, &quot;Finish&quot; and &quot;Uninstall&quot; steps can now run shell scripts;</li>
            <li>Install can now add or remove login items (from within the &quot;Copy files&quot; and &quot;Uninstall&quot; steps);</li>
            <li>In the event of an error while running an operation with root privileges, Install now logs more detailed information about the problem;</li>
            <li>Install's application menu now contains an item for going to the Install website;</li>
            <li>Cleaned up the user interface by disabling the contextual menu when clicking certain types of text; it is still possible to select text and use a contextual menu when looking at &quot;ReadMe&quot; files or licenses;</li>
          </ul>
          
          <h4><a id="1.0.5" name="1.0.5"></a>Version 1.0.5 (17 February 2004)</h4>
          
          <ul>
            <li>Added &quot;SIGHUP&quot; as an additional process termination method;</li>
            <li>More layout corrections in Japanese localization.</li>
          </ul>
          
          <h4><a id="1.0.4" name="1.0.4"></a>Version 1.0.4 (6 February 2004)</h4>
          
          <ul>
            <li>Layout correction in Japanese localization.</li>
          </ul>
          
          <h4><a id="1.0.3" name="1.0.3"></a>Version 1.0.3 (1 January 2004)</h4>
          
          <ul>
            <li>Japanese localization by Akira Ikeda.</li>
          </ul>
          
          <h4><a id="1.0.2" name="1.0.2"></a>Version 1.0.2 (18 December 2003)</h4>
          
          <ul>
            <li>Recompiled on Panther using Xcode; like previous releases, this version is qualified for use on both Panther and Jaguar;</li>
            <li>Fixed potential problems when attempting to run Install for the current user only from a directory where the path included special characters (such as &quot;&#402;&quot;);</li>
            <li>Fixed potential problems when installing for all users: if a file operation failed (which in itself is extremely unlikely in the default configuration) it would fail silently;</li>
            <li>The lock/unlock button is now only ghosted at critical points, bringing the user interface in line with the pattern defined by Apple's System Preferences application;</li>
            <li>New menu item, &quot;Reveal files on disk&quot;, to help users who wish to perform manual (drag-and-drop) installations; for clarity, renamed &quot;Show files&quot; menu it to, &quot;Show files list&quot;;</li>
            <li>After an uninstallation, there are now two buttons available (&quot;Go Back&quot; and &quot;Quit&quot;); previously there was only one (&quot;Go Back&quot;);</li>
            <li>Added Spanish localization by Wincent Colaiuta;</li>
            <li>The &quot;Files&quot; directory name now appears as localized (English, German or Spanish) when viewed in the Finder (but only if the &quot;Always show file extensions&quot; option is disabled in the Finder preferences);</li>
            <li>Internal updates to string handling in line with Apple recommendations;</li>
            <li>Choosing &quot;About Install&quot; from the &quot;Install&quot; menu now shows engineering and localization credits;</li>
            <li>German localization updated (by Felix K&uuml;hne) and various errors corrected and adjustments made;</li>
            <li>Fixed problems on attempting to resize uninstall window during or after an uninstall operation;</li>
            <li>&quot;Uninstall&quot; button now ghosted when no checkboxes selected in the uninstall files listing;</li>
            <li>Files list in the &quot;Uninstall&quot; window now updates dynamically; for example, deselecting a file turns the name light gray; selecting it turns the name black; files in the process of being removed are shown in dark gray; successfully removed files appear with a &quot;strikethrough&quot; font;</li>
            <li>Fixed display bug when clicking on the lock button text (previously, a subtle white highlight box was visible when clicking on the text);</li>
            <li>In line with Apple's conventions, clicking on the lock button text is now equivalent to clicking on the lock button itself; this is especially helpful in non-English localizations such as Spanish, where the text actually reads &quot;Click here to make changes&quot;.</li>
          </ul>
          
          <h4><a id="1.0.1" name="1.0.1"></a>Version 1.0.1 (2 September 2003)</h4>
          
          <ul>
            <li>Recompiled with GCC 3.3 and made updates to codebase to eliminate new warnings;</li>
            <li>Fixed potential problems with &quot;Launch&quot; button;</li>
            <li>German localization by Felix K&uuml;hne.</li>
          </ul>
          
          <h4><a id="1.0" name="1.0"></a>Version 1.0 (26 June 2003)</h4>
          
