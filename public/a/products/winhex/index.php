          <h3>Introducing WinHex</h3>
          <img
            style="float:left; margin: 20px; "
            src="../../gfx/winhex-icon.jpg"
            alt="WinHex icon"
            width="128"
            height="128"
          />
          <p>
            WinHex is a flexible number conversion utility for programmers and
            other people who work with numbers in a digital setting. It
            converts, in real time, any number in any base representation from
            Base 2 (Binary) through to Base 36. Numbers can be arbitrarily
            large, limited only by the availability of system resources.
          </p>

          <p>
            By default, the main window provides spaces for the entry and
            display of Hexadecimal, Decimal (Base 10), Octal (Base 8) and Binary
            representations, as well as an ASCII (7-bit text) representation and
            a general field that can be used to display any base from Base 2
            through to Base 36.
          </p>

