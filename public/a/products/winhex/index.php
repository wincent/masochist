          <h4>System requirements</h4>

          <p>WinHex requires Mac OS X 10.3 or later</p>

          <h4>Installation and removal</h4>

          <p>To install WinHex, drag it to your preferred location.</p>

          <p>To remove it, drag it to the Trash.</p>

          <h4>Known issues</h4>

          <ul>
            <li>
              On entering the ASCII text field, any
              <a href="faq/">gray formatting</a> applied to non-7-bit characters
              in the field is removed and is not reapplied until the cursor is
              moved into another field and the field altered.
            </li>

            <li>
              Similarly for <a href="faq/">control character formatting</a>, the
              gray caret symbols are removed (the actual, inivisible, control
              characters occur in the field instead of the placeholders) on
              entering the ASCII text field and re-appear on leaving the field.
            </li>

            <li>
              When you edit the ASCII text field under either of these
              cirucmstances, results may be unpredictable because the control
              characters, although present in the field, are not visible to the
              user. Also, changing the field causes the gray question marks to
              be treated as literal 7-bit question mark characters, effectively
              changing the value of the represented number.
            </li>

            <li>
              All of these issues should be resolved by the end of the beta
              testing period.
            </li>
          </ul>

