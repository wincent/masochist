          <h4>Hextrapolate</h4>
          
          <a href="hextrapolate/"><img style="float:left; margin: 20px; " src="https://wincent.com/a/gfx/winhex-icon-medium.jpg" alt="Hextrapolate icon" width="64" height="64" /></a>
          <p><a href="hextrapolate/">Hextrapolate</a> (formerly WinHex) is a powerful number conversion utility with an interface so intuitive that you'll never even think of reading the instructions.</p>
          
          <p>Programmers will find that Hextrapolate's compact, uncluttered window provides the best means to instantly convert between hexadecimal, decimal, octal, binary and string representations, and go much further... Unlike other conversion applications, Hextrapolate isn't limited to 32 or 64 bit numbers; you can go as far as your system resources will allow, even to numbers that are thousands of bits in size. And similarly, feel free to stretch beyond the common bases and play with any base from 2 to 36 inclusive.</p>
          
