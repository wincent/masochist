          <h4>Install</h4>
          
          <a href="install/"><img style="float:left; margin: 20px; " src="https://wincent.com/a/gfx/install-icon-medium.jpg" alt="Install icon" width="64" height="64" /></a>
          <p>A robust, easy-to-use alternative to Apple's own Installer application, <a href="install/">Install</a> provides developers with all the power needed to deploy software on Mac OS X, but conveniently bypasses the need to assemble package files and write installation scripts.</p>
          
          <p>Your users will find installing your software a breeze, and you'll save yourself valuable time by not having to answer "How do I install?" queries.</p>
          
          <p>Install is easily configured through a single XML property list, and custom user interface elements and text can be inserted using RTF documents.</p>
          
          <p>Install can neatly handle all the "pre-flight" details of an installation, such as gracefully exiting target applications, moving items to the Trash, or deleting files and folders. It can install with or without root privileges, and for the current user or all users on a target machine. And if your users decide they'd like to remove your software, Install can handle that as well.</p>
          
