          <h4>WinSwitch</h4>
          
          <a href="winswitch/"><img style="float:left; margin: 20px; " src="https://wincent.com/a/gfx/winswitch-icon-medium.jpg" alt="WinSwitch icon" width="64" height="64" /></a>
          <p>Apple did a superb job with its new Fast User Switching feature in Panther (Mac OS X 10.3), but for users with long names, the associated menu item is a terrible waste of precious Menu Bar real estate. WinSwitch fixes that and brings a number of other powerful features that are not available on other Panther or Tiger (Mac OS X 10.4).</p>
          
          <img style="float:right; margin: 20px; " src="../gfx/winswitch-preview.gif" alt="WinSwitch preview" width="57" height="44" />
          <p>Wouldn't it be nice if you could access the feature through a compact, icon-sized Menu Bar icon? <a href="winswitch/">WinSwitch</a> does exactly that, closely matching the performance and style of the Apple-provided menu, but without all the bulk. It can display a generic icon in the menu bar (as shown), your user picture, or the full or short username. You can customize the display size of your user picture in the menu bar, separately set the size of pictures inside the menu itself, set a hot key to jump back to the login window, or elect to show the "root" user as well, <a href="winswitch/">among many other features</a>.</p>
          
